// Accept three numbers and compute their average
#include<iostream>
using namespace std;
int main(){
    float marks1,marks2,marks3;
    cout<<"Enter the marks of three students ";
    cin>>marks1>>marks2>>marks3;
    cout<<"The average of the three students is "<<(marks1+marks2+marks3)/3;
    return 0;
}

